#include "push_swap_bonus.h"

void	sa_b(t_stack **a)
{
	t_stack	*tmp;

	if (!a || !(*a))
		return ;
	if (*a && (*a)->next)
	{
		tmp = *a;
		*a = (*a)->next;
		tmp->next = (*a)->next;
		(*a)->next = tmp;
	}
}
