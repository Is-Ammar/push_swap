#include "push_swap_bonus.h"

void	sa_b(t_stack **sa)
{
	t_stack	*tmp;

	if (*sa && (*sa)->next)
	{
		tmp = *sa;
		*sa = (*sa)->next;
		tmp->next = (*sa)->next;
		(*sa)->next = tmp;
	}
}
