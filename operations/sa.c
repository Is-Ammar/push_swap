#include "../push_swap.h"

void sa(t_stack **sa)
{
    t_stack *tmp;

    if (*sa && (*sa)->next)
    {
        tmp = *sa;
        *sa = (*sa)->next;
        tmp->next = (*sa)->next;
        (*sa)->next = tmp;
    }
    write(1, "sa\n", 3);
}